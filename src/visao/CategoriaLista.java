public void atualizaCategoria(){
        CategoriaDAO dao = new CategoriaDAO();
        ArrayList<Categoria> categorias = dao.listar();
        tab = (DefaultTableModel) tabela.getModel();
        
        while(tab.getRowCount() > 0){
            tab.removeRow(0);
        }
        int i = 0;
        for(Categoria c : categorias){
            tab.addRow(new String[i]);
            tab.setValueAt(c.getCodigo(),i,0);
            tab.setValueAt(c.getNome(),i,1);
            i++;
        }
    }
